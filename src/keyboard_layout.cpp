std::optional<KeyPosition> getKeyPosition(int vkCode) {
    const auto it = std::find_if(kKeyboardLayout.begin(), kKeyboardLayout.end(), [&](const KeyLayoutEntry& entry) {
        return entry.code == vkCode;
    });

    if (it == kKeyboardLayout.end()) {
        return std::nullopt;
    }

    return KeyPosition{
        it->x + it->width / 2.0,
        it->y + it->height / 2.0,
        it->width,
        it->height,
    };
}

